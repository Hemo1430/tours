export let exchangeRates: Record<Currency, number> = {
  EGP: 1,
  USD: 0.0206, // High-quality default approximation
  SAR: 0.0773, // High-quality default approximation
};

export const formatPrice = (priceEGP: number, currency: Currency, lang: Lang) => {
  const rate = exchangeRates[currency] || 1;
  const converted = priceEGP * rate;
  
  // Format specifically based on language and specific requirements
  // E.g. make the prices looks like 1.02 -> give me the numbers after the point
  // when I choose SAR the letters next to the price become SAR and when dollar they become $ and when EGP it becomes EGP.
  const formatter = typeof Intl !== 'undefined' 
      ? new Intl.NumberFormat('en-US', { minimumFractionDigits: 2, maximumFractionDigits: 2 })
      : null;
      
  const formattedNumber = formatter ? formatter.format(converted) : converted.toFixed(2);
  
  if (currency === 'EGP') return `${formattedNumber} EGP`;
  if (currency === 'USD') return `${formattedNumber} $`;
  if (currency === 'SAR') return `${formattedNumber} SAR`;
  
  return `${formattedNumber} ${currency}`;
}
